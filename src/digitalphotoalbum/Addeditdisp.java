    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(!flag){
            JOptionPane.showMessageDialog(aed, "Choose an image file!");
            return;
        }
        String Title = title.getText();
        if(Title.isEmpty() || Title.length() > 20){
            JOptionPane.showMessageDialog(aed, "Enter a title (1 - 20 chars)");
            return;
        }
        String Anno = anno.getText();
        if(Anno.isEmpty() || Anno.length() > 100){
            JOptionPane.showMessageDialog(aed, "Enter an annotation (1 - 100 chars)");
            return;
        }
        x.setTitle(Title);
        x.setAnno(Anno);
        o.add(x);
        aed.dispose();
        mg.displayList();
        if(o.size() == 1)
            p.setText("Select an image");
        mg.setVisible(true);
    }//GEN-LAST:event_jButton2ActionPerformed

